import { Elysia } from 'elysia'
import prisma from '../lib/prisma.js'
import jwt from 'jsonwebtoken'

// JWT Token Verification
const verifyToken = async (token) => {
  try {
    return jwt.verify(token, process.env.JWT_SECRET)
  } catch (error) {
    throw new Error(`Token ‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á: ${error.message}`)
  }
}

// Core JWT Middleware
// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô middleware ‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏µ‡πà inject user data ‡πÄ‡∏Ç‡πâ‡∏≤ context
export const jwtMiddleware = new Elysia()
  .derive(async ({ headers }) => {
    console.log('üîç JWT Middleware: ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö headers...', headers.authorization ? '‡∏û‡∏ö Token' : '‡πÑ‡∏°‡πà‡∏û‡∏ö Token')
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö Authorization header
    const authHeader = headers.authorization
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      console.log('‚ùå JWT Middleware: ‡πÑ‡∏°‡πà‡∏û‡∏ö Bearer token ‡∏ó‡∏µ‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á')
      return { user: null }
    }

    try {
      // ‡πÅ‡∏¢‡∏Å‡πÅ‡∏•‡∏∞‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö token
      const token = authHeader.substring(7)
      console.log('üîì JWT Middleware: ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö token...')
      const decoded = await verifyToken(token)
      console.log('‚úÖ JWT Middleware: ‡∏ñ‡∏≠‡∏î‡∏£‡∏´‡∏±‡∏™ token ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à:', { userId: decoded.userId, email: decoded.email })

      // ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏à‡∏≤‡∏Å‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
      console.log('üîç JWT Middleware: ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ID:', decoded.userId)
      const user = await prisma.users.findUnique({
        where: { user_id: decoded.userId },
        select: {
          user_id: true,
          email: true,
          first_name: true,
          last_name: true,
          citizen_id: true,
          position: true,
          department: true,
          zip_code: true,
          created_at: true,
          updated_at: true,
          roles: {
            select: {
              role_name: true,
              role_status: true
            }
          }
        }
      })

      console.log('üìã JWT Middleware: ‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏à‡∏≤‡∏Å‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•:', user ? '‡∏û‡∏ö‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ' : '‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ')
      
      if (!user) {
        console.log('‚ùå JWT Middleware: ‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•')
        return { user: null }
      }

      // **ENHANCEMENT**: Inject user.role ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏á‡πà‡∏≤‡∏¢‡∏ï‡πà‡∏≠‡∏Å‡∏≤‡∏£‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á
      const userWithRole = {
        ...user,
        role: user.roles?.role_name || null
      }

      console.log('‚úÖ JWT Middleware: ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏û‡∏£‡πâ‡∏≠‡∏°‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô:', userWithRole)
      // Return user data ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Elysia context
      return { user: userWithRole }

    } catch (error) {
      console.error('‚ùå JWT Error:', error.message)
      return { user: null }
    }
  })

